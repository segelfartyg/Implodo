// --- Helpers ---

func (h *Handler) fetchGoogleUserInfo(token *oauth2.Token) (*googleUserInfo, error) {
	client := h.oauthConfig.Client(context.Background(), token)
	resp, err := client.Get("https://www.googleapis.com/oauth2/v3/userinfo")
	if err != nil {
		return nil, err
	}
	defer resp.Body.Close()

	body, err := io.ReadAll(resp.Body)
	if err != nil {
		return nil, err
	}

	var info googleUserInfo
	if err := json.Unmarshal(body, &info); err != nil {
		return nil, err
	}
	return &info, nil
}

func (h *Handler) isAllowed(googleID string) bool {
	for _, id := range h.cfg.AllowedGoogleIDs {
		if id == googleID {
			return true
		}
	}
	return false
}

// verifyCodeChallenge checks BASE64URL(SHA256(verifier)) == challenge.
// Uses constant-time comparison to avoid timing attacks.
func verifyCodeChallenge(verifier, challenge string) bool {
	h := sha256.Sum256([]byte(verifier))
	computed := base64.RawURLEncoding.EncodeToString(h[:])
	return subtle.ConstantTimeCompare([]byte(computed), []byte(challenge)) == 1
}
